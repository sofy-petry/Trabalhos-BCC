/*ex 2.1
Desenvolva um programa em Java capaz de imprimir todos os números entre 0 e 100 (inclusive). Os números devem ser separados por espaços, e não deve haver quebra de linha entre eles.
*/
public class main21{
    public static void main(String args[]){
        int N =0;
        while(N<101){
            System.out.print(N + " ");
            N++;
        }
    }
}